# 1. Deklariere und initialisiere Variablen für Name, Alter und Lieblingsessen

name = "Max"               # String
alter = 25                 # Integer
lieblingsessen = "Pizza"   # String

# 2. Gib die Informationen in einem Satz aus (unter Verwendung eines f-Strings)

# Der f-String ermöglicht es, die Variablenwerte direkt in den Ausgabetext einzufügen.
ausgabe_satz = f"Hallo {name}, du bist {alter} Jahre alt und dein Lieblingsessen ist {lieblingsessen}."

# Gib den Satz in der Konsole aus
print(ausgabe_satz)