# Eine Variable für jeden der vier grundlegenden Datentypen erstellen

# Integer (ganze Zahl)
ganze_zahl = 42

# Float (Fließkommazahl)
komma_zahl = 3.14

# String (Text)
text = "Hallo Welt"

# Boolean (Wahrheitswert)
wahrheitswert = True

# Den Datentyp jeder Variable mit der Funktion type() ausgeben
print("Der Datentyp von ganze_zahl ist:", type(ganze_zahl))
print("Der Datentyp von komma_zahl ist:", type(komma_zahl))
print("Der Datentyp von text ist:", type(text))
print("Der Datentyp von wahrheitswert ist:", type(wahrheitswert))